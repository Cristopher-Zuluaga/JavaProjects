public class Relational1 {
  public static void main(String[] args) 
  {
    double creditsEarned = 176.5;
    double creditsForSeminar = 8;
    double creditsToGraduate = 180;
    double creditsAfterSeminar = creditsEarned + creditsForSeminar;
    System.out.println(creditsEarned > creditsToGraduate);
    System.out.println(creditsToGraduate < creditsAfterSeminar);
    
  }
}
